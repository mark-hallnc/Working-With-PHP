<p>PHP was created in 1994 by Rasmus Lerdorf. PHP originally stood for “Personal Home Page” because he used it to maintain his personal page, which had originally been written in C. It was when he introduced web forms and database communication to his personal homepage that PHP was born. 

PHP is a language used to make websites dynamic and interactive. It takes HTML to the next level. It is an object-oriented programming language that can be used on its own or in web design. The difference between PHP and other languages is that it is executed on the server side. When a user makes a request to a web server, for example clicking on a hyperlink, the web server returns the HTML file to the user (displays the webpage). But if the web server finds any PHP extensions in the request, it executes that code and returns the result to the person who requested it. 
