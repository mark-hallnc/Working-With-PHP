<p>
My journey to web design began in Spring of 2020. Due to Covid-19 I suddenly found myself with nothing to do in my spare time. I had always had a passing interest in computer programming, but up to that point hadn’t explored it. During my new downtime I decided to sign up for a free online coding course. The course started with HTML and CSS, which I completed within a few weeks. I then progressed into Javascript, which I found interesting but tough. When I ran into a roadblock, I decided that if I were going to proceed, I was going to need some formal training. In summer of 2020 I began my journey towards a degree in Computer Programming at GTCC. 
Since my first courses at GTCC I have taken a web design course studying HTML, CSS and databases. I have also taken classes in the programming languages Python, Java, Swift and PHP, as well as a course in networking and security. So far, I enjoy learning programming languages the most. The best part is taking a blank canvas, writing some code and having a working program at the end. I like the challenge and find it rewarding to be able to create something from nothing.
A year or two from now I hope to be in my first programming job, working on turning ideas into reality. I don’t care if its developing banking software or building iOS applications. I just enjoy the process. 
