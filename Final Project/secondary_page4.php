<p>
PHP is what makes websites dynamic. It is open source and widely used for web development. Have you ever noticed that often websites look different depending on the device you are using to view it? In most instances, any website you visit where you sign up for an account uses PHP. It is used in the forms you fill out, and it talks to the databases that gather and store your information. Think of a program or application that is installed on your computer. It lives on your computer, and your computer runs it when you open it. PHP is not that. PHP is installed on web servers. When you visit a website, the web server that serves that website up to you runs the PHP program and sends the results to you in the form of a web page. There are more than 20 million websites on the internet using PHP. 
